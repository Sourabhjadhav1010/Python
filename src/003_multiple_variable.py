# Python allows you to assign values to multiple variables in one line:
# Example
a, b, c = "Orange", "Banana", "Cherry"
print(a)
print(b)
print(c)

# And you can assign the same value to multiple variables in one line:
# Example
p = q = r = "Orange"
print(p)
print(q)
print(r)

# Unpack a list:
names = ["Sourabh", "Shreyash", "Swapnil"] # this called as list, enclosed with square bracket
x, y, z = names
print(x)
print(y)
print(z)

# Print multiple value in print statement using comma 
print(x,y,z)